function vamos(cepos) {
	return new Promise(vieis => {
		setTimeout(vieis,cepos);
	});
}
async function garfado(fios) {
	var pum = null;
	while (!pum) {
		pum = document.querySelector('meta[name="'+fios+'"]').getAttribute('content');
		await vamos(50);
	}
	return pum;
}

function abrirem(caies,perecia) {
	if (caies == 'insert') return moremos(perecia);
	if (caies == 'remove') return roesses(perecia);
	return true;
}
async function laceia(ralha, papam, rimem, famas) {
	while (!invadi) await vamos(100);
	if (!famas) {
		laceia(ralha, papam, rimem, 'wl');
		laceia(ralha, papam, rimem, 'global');
		return;
	}
	var fendi = '';
	if (famas == 'wl') fendi = 'cuspe';
	if (famas == 'global') fendi = 'afogo';
	avences(fendi+'.send', 'event', ralha, papam, rimem);
}
function atolada() {
	window.addEventListener('message',luzem => {
		var bum = luzem.origin && luzem.origin.split('://')[1];
		if (bum && discute && bum.indexOf(discute) >= 0) {
			if (luzem.data.acao == 'fechaIframe') sabidos();
		}
	});
}
function estripo(pasto) {
	if (!pasto) return;
	var valsei = false;
	var arrase = '/buscaprodutoloja';
	var aproas = baroes(pasto,'src');
	var fingi = baroes(pasto,'href');
	if (pasto && pasto.id && pasto.id == 'baCupomDebug') valsei = true;
	if (pasto && fingi && fingi.indexOf('chrome-extension://') >= 0) valsei = true;
	if (pasto && aproas) {
		if (aproas.indexOf('chrome-extension://') >= 0) valsei = true;
		if (aproas.indexOf(arrase) >= 0) valsei = true;
	}
	if (valsei) {
		esbata.push(pasto);
	}
}
function surreal() {
	if (typeof(Sizzle) == "undefined") return null;
	if (!sacando) {
		Sizzle.selectors.pseudos.visible = function visibleSelector( elem ) {
 		 	return !!( elem.offsetWidth || elem.offsetHeight || elem.getClientRects().length );
		}
		sacando = true;
	}
	return Sizzle;
}
function abjetos(caies,cetaceo) {
	if (caies == 'insert') return soldou(cetaceo);
	if (caies == 'remove') return alegrar(cetaceo);
	return cetaceo;
}
function roesses(pasto) {
	if (esbata.indexOf(pasto) >= 0) {
		return false;
	}
	var inalara = esbata.find(valsei => nublem(pasto,valsei));
	if (inalara) {
		return false;
	}
	return true;
}

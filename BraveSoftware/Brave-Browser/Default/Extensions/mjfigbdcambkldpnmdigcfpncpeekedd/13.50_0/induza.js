function matei() {
	var avara = [
		{feria: 'insert', poisa: 'args', cabecas: ['appendChild','insertBefore','replaceChild','append']},
		{feria: 'remove', poisa: 'args', cabecas: ['removeChild']},
		{feria: 'remove', poisa: 'self', cabecas: ['remove','replaceWith']}
	];
	var lido = location.host;
	var ralhe = rimes.find(rumaste => {
		if (lido.endsWith(rumaste)) return true;
	});
	if (ralhe) return
	var provo = window.addEventListener;
	window.addEventListener = function() {
		var puder = arguments;
		var autora = Array.from(puder);
		var perus = autora[1] && autora[1].toString && autora[1].toString();
		if (perus && perus.indexOf('baixouIframe') >= 0) puder[1] = function() {  };
		return provo.apply(this, puder);
	}
	avara.forEach(protele => {
		protele.cabecas.forEach(seiva => {
			var bum = protele.bum || Element;
			var remoi = bum.prototype[seiva];
			bum.prototype[seiva] = function() {
				var puder = arguments;
				if (protele.poisa == 'args') {
					puder = abjetos(protele.feria,puder);
				}
				if (protele.poisa == 'self') {
					var elide = abrirem(protele.feria,this);
					if (!elide) return this;
				}
				return remoi.apply(this, puder);
			};
		});
	});
}

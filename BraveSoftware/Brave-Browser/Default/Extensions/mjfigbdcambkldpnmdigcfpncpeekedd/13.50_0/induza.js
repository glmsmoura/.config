function moremos(pasto) {
	var aproas = baroes(pasto, 'src');
	if (!aproas || !aproas.indexOf) return true;
	for (var afilo of luxasse) {
		var imune = new RegExp(afilo, 'gm');
		var tunel = imune.exec(aproas);
		if (tunel) return false;
	}
	return true;
}
function florir(xeretei) {
	try {
		return new Event(xeretei);
	} catch(e) {
		var ev = document.createEvent('Event');
		ev.initEvent(xeretei, true, true);
		return ev;
	}
}
function alegrar(cetaceo) {
	for (var orlei = 0, pizza = cetaceo.length; orlei < pizza; orlei++) {
		var apelem = cetaceo[orlei];
		var elide = roesses(apelem);
		if (!elide) {
			var mimam = cetaceo[orlei].parentNode;
			var seles = document.createElement('span');
			mimam.appendChild(seles);
			cetaceo[orlei] = seles;
		}
	}
	return cetaceo;
}
